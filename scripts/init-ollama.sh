#!/bin/bash
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# Ollama Model Initialization Script
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# Waits for the Ollama server to be ready, then pulls the model.
# Used as a one-shot init container in Docker Compose.
#
# Environment Variables:
#   OLLAMA_HOST      - Ollama server URL (default: http://ollama:11434)
#   OLLAMA_MODEL_ID  - Model to pull (default: phi4-mini)
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

set -e

OLLAMA_HOST="${OLLAMA_HOST:-http://ollama:11434}"
OLLAMA_MODEL_ID="${OLLAMA_MODEL_ID:-qwen2.5:3b}"

echo "â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•"
echo "  Ollama Model Initialization"
echo "  Host:  ${OLLAMA_HOST}"
echo "  Model: ${OLLAMA_MODEL_ID}"
echo "â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•"

# â”€â”€ Wait for Ollama server to be ready â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
echo "â³ Waiting for Ollama server at ${OLLAMA_HOST}..."

MAX_RETRIES=60
RETRY_INTERVAL=5
RETRIES=0

until ollama list > /dev/null 2>&1; do
    RETRIES=$((RETRIES + 1))
    if [ "$RETRIES" -ge "$MAX_RETRIES" ]; then
        echo "âœ˜ Ollama server not ready after $((MAX_RETRIES * RETRY_INTERVAL))s â€” aborting"
        exit 1
    fi
    echo "  Retry ${RETRIES}/${MAX_RETRIES}..."
    sleep "$RETRY_INTERVAL"
done

echo "âœ“ Ollama server is ready"

# â”€â”€ Check if model is already available â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
if ollama list | grep -q "${OLLAMA_MODEL_ID}"; then
    echo "âœ“ Model '${OLLAMA_MODEL_ID}' is already available â€” skipping pull"
    exit 0
fi

# â”€â”€ Pull the model â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
echo "ğŸ“¥ Pulling model '${OLLAMA_MODEL_ID}'... (this may take several minutes)"
ollama pull "${OLLAMA_MODEL_ID}"

echo "âœ“ Model '${OLLAMA_MODEL_ID}' pulled successfully"
echo "â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•"
